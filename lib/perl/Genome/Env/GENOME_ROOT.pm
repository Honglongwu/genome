package Genome::Env::GENOME_ROOT;

=pod

=head1 NAME

GENOME_ROOT

=head1 DESCRIPTION

The GENOME_ROOT is the directory at which GMS systems are attached, typically /opt/gms/

The current GMS is attached under this directory, at $GENOME_ROOT/$GENOME_SYS_ID.

=head1 DEFAULT VALUE

/opt/gms/

=cut

1;
